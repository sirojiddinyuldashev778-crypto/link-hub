window.changeLang = function(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if(dict[lang] && dict[lang][key]) {
      if (el.tagName === "A" && el.querySelector("i")) {
        // preserve icon inside A tags (like the hero telegram button)
        const icon = el.querySelector("i").outerHTML;
        if(key.startsWith("co") && key.endsWith("b")) {
            el.innerHTML = dict[lang][key] + " " + icon;
        } else if(key.startsWith("h-btn1")) {
            el.innerHTML = icon + " " + dict[lang][key];
        } else {
            el.innerHTML = dict[lang][key];
        }
      } else {
        el.textContent = dict[lang][key];
      }
    }
  });
  
  const currentLangText = document.getElementById("current-lang-text");
  const currentLangImg = document.getElementById("current-lang-img");
  if(currentLangText && currentLangImg) {
      if(lang === "uz") { currentLangText.textContent = "UZ"; currentLangImg.src = "https://flagcdn.com/w40/uz.png"; }
      if(lang === "ru") { currentLangText.textContent = "RU"; currentLangImg.src = "https://flagcdn.com/w40/ru.png"; }
      if(lang === "en") { currentLangText.textContent = "EN"; currentLangImg.src = "https://flagcdn.com/w40/us.png"; }
  }
  localStorage.setItem("lang", lang);

  // Close lang dropdown after selection
  const langSwitchEl = document.querySelector('.lang-switch');
  if(langSwitchEl) langSwitchEl.classList.remove('open');
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("lang") || "uz";
  window.changeLang(savedLang);
});
